
public class Person
{
    String name;
    String IP;
    
    
    
    public void setName(String a)
    {
        name = a;
    }
    public String getName()
    {
        return name;
    }
    
    public void setIP(String a)
    {
        IP = a;
    }
    public String getIP()
    {
        return IP;
    }
    
    public String toString()
    {
        return name;
    }
}